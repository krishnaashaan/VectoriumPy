# Core Calculations

# Point charge: electric_field_point_charge() — E-field vector at any point from a point charge.

# Line charge: electric_field_line() — finite and infinite line charge formulas.

# Ring/disk/plane: electric_field_ring(), electric_field_disk(), electric_field_infinite_sheet().

# Dipole fields: electric_field_dipole() and far-field approximations.

# Continuous distributions: API to compute E from rho(x) or discrete charge arrays (numerical integration).